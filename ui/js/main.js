function $(sel, root) { return (root || document).querySelector(sel); }
function $$(sel, root) { return Array.from((root || document).querySelectorAll(sel)); }

function el(tag, attrs) {
  attrs = attrs || {};
  const children = Array.prototype.slice.call(arguments, 2);
  const node = document.createElement(tag);
  for (var k in attrs) {
    if (!Object.prototype.hasOwnProperty.call(attrs, k)) continue;
    var v = attrs[k];
    if (k === 'class') node.className = v;
    else if (k === 'style' && typeof v === 'object') Object.assign(node.style, v);
    else if (k.indexOf('on') === 0 && typeof v === 'function') node.addEventListener(k.slice(2).toLowerCase(), v);
    else if (k === 'dataset' && typeof v === 'object') Object.assign(node.dataset, v);
    else if (v != null) node.setAttribute(k, v);
  }
  for (var i = 0; i < children.length; i++) {
    var child = children[i];
    if (child == null || child === false) continue;
    if (Array.isArray(child)) { for (var j = 0; j < child.length; j++) el.call(null, 'span', {}, child[j]); continue; }
    if (typeof child === 'string' || typeof child === 'number') {
      node.appendChild(document.createTextNode(String(child)));
    } else {
      node.appendChild(child);
    }
  }
  return node;
}

function clear(node) { while (node.firstChild) node.removeChild(node.firstChild); }

function formatRupiah(n) {
  if (!n || n === 0) return '—';
  return 'Rp' + n.toLocaleString('id-ID');
}

function pct(n) { return Math.round(n * 100) + '%'; }

function debounce(fn, ms) {
  ms = ms || 200;
  var t;
  return function() {
    var args = arguments;
    clearTimeout(t);
    t = setTimeout(function() { fn.apply(null, args); }, ms);
  };
}

function getQueryParam(key) {
  return new URLSearchParams(location.search).get(key);
}

function getQueryParams() {
  return Object.fromEntries(new URLSearchParams(location.search).entries());
}

function setQueryParams(params) {
  var u = new URLSearchParams(location.search);
  for (var k in params) {
    if (params[k] == null) u.delete(k);
    else u.set(k, params[k]);
  }
  var s = u.toString();
  history.replaceState(null, '', s ? '?' + s : location.pathname);
}

function toast(message, ms) {
  ms = ms || 2400;
  var t = el('div', { class: 'toast' }, message);
  document.body.appendChild(t);
  setTimeout(function() { t.remove(); }, ms);
}

function getStorage(key, fallback) {
  try {
    var v = localStorage.getItem('karbuin:' + key);
    return v != null ? JSON.parse(v) : (fallback == null ? null : fallback);
  } catch (e) { return fallback == null ? null : fallback; }
}

function setStorage(key, value) {
  try { localStorage.setItem('karbuin:' + key, JSON.stringify(value)); } catch (e) {}
}

